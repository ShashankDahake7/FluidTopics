export function isoToFlagEmoji(code) {
  if (!code || code === 'ZZ' || String(code).length !== 2) return '';
  const cc = String(code).toUpperCase();
  const A = 0x1f1e6;
  return String.fromCodePoint(A + cc.charCodeAt(0) - 65, A + cc.charCodeAt(1) - 65);
}
